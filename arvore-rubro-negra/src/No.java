public class No {
    private int valor;
    private No pai;
    private No filhoDireita;
    private NO filhoEsquerda;
    private String cor;
    private char duploN;

    public int getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }


    public No getPai() {
        return pai;
    }

    public void setPai(No pai) {
        this.pai = pai;
    }


    public No getFilhoDireita() {
        return filhoDireita;
    }

    public void setFilhoDireita(No filhoDireita) {
        this.filhoDireita = filhoDireita;
    }


    public NO getFilhoEsquerda() {
        return filhoEsquerda;
    }

    public void setFilhoEsquerda(NO filhoEsquerda) {
        this.filhoEsquerda = filhoEsquerda;
    }


    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }


    public char getDuploN() {
        return duploN;
    }

    public void setDuploN(char duploN) {
        this.duploN = duploN;
    }

    
}
